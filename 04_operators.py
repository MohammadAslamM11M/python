# Arithmetic operators
a = 7
b = 4
c = a + b
print(c)

# Assignment operators
a = 4-2     # Assign 4-2 in a
b = 6
b *= 3      # Multiply the value of b by 3 and then assign it to b
print(b)

# Comparison operators
d = 5 > 4
print(d)

a = 3 < 4
print(a)

b = 2 >= 7
print(b)

c = 5 <= 5
print(c)

e = 10 == 10
print(e)

f = 5 != 2
print(f)
